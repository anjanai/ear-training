let lastdir = '';
let samples = [];
let instruments = [];
let urls = {};
let tone_sampler = {};

for (let i of files) {
    [dir, file] = i.split('/');
    if (dir != lastdir) {
	instruments.push(dir);
	if (lastdir != '') {
	    tone_sampler.urls = urls;
	    samples.push(tone_sampler);
	}
	tone_sampler = {};
	//sampler.baseUrl = "../tonejs-instruments/samples/" + dir + "/";
	tone_sampler.baseUrl = "https://nbrosowsky.github.io/tonejs-instruments/samples/" + dir + "/";
	
	lastdir = dir;
	urls = {};
    }
    let notename = file.split('.')[0].replace('s','#');
    urls[notename] = file;
}

tone_sampler.urls = urls;
samples.push(tone_sampler);

