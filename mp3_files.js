lastdir = '';
samples = [];
instruments = [];
urls = {};
for (i of files) {
    [dir, file] = i.split('/');
    if (dir != lastdir) {
	instruments.push(dir);
	if (lastdir != '') {
	    sampler.urls = urls;
	    samples.push(sampler);
	}
	sampler = {};
	//sampler.baseUrl = "../tonejs-instruments/samples/" + dir + "/";
	sampler.baseUrl = "https://nbrosowsky.github.io/tonejs-instruments/samples/" + dir + "/";
	
	lastdir = dir;
	urls = {};
    }
    let notename = file.split('.')[0].replace('s','#');
    urls[notename] = file;
}

sampler.urls = urls;
samples.push(sampler);



function add_instruments() {
    for (let i in instruments) {
	$("#instru-select").append(new Option(instruments[i], i));
    }
    $("#instru-select").change(function(){ changeInstrument(this.value);  });
    $("#instru-select").val(13);
}


